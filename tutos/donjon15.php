<span id="100"><h1>Donjon élite (n°15) - Le bagne des esclavagistes</h1></span>
<font color="red"><b>Localisation :</b></font> Uniquement accessible aux joueurs ayant fini le jeu. Le donjon situé au Sud-est des Cascades verdoyantes est constitué de 4 sous-donjons tous accessibles à partir du même niveau.<br />
<br />
<div align="center"><img src="img/bagne/bagneel7.jpg" alt="localisation bagne des esclavagistes" /></div><br />
<font color="red"><b>Quête relative : </b></font>"Le dernier Hiérophante " donnée par Veth le conteur au Bagne des esclavagistes.<br />
Temps : <img src="img/ico_duree.png" alt="icône de durée" /><img src="img/ico_duree.png" alt="icône de durée" /><img src="img/ico_duree.png" alt="icône de durée" /><img src="img/ico_duree.png" alt="icône de durée" /><br />
Difficulté : <img src="img/difficile.png" alt="icône difficulté difficile" /><br /><br />
<font color="red"><b>Objectifs :</b></font> Ce grand donjon est en fait composé de 4 donjons à l'intérieur (il vaut mieux retourné en ville après en avoir fini un). La quête consiste à tuer 5 boss (le 1er donjon en contient deux), chaque boss donne un coffre et il faut avoir tuer les 4 premiers boss pour avoir accès au dernier donjon.<br /><br />
<font color="red"><b>Points gagnés :</b></font> 5.000 points Norn en mode normal, 7.500 points Norn en mode difficile et beaucoup de points Nain. La récompense de la quête est de 15.000 pts d'expérience et 2k750.<br /><br />
<u><b>Autres points importants :</b></u>
<ul>
	<li>Le groupe peut entrer dans ce donjon si au moins un membre a terminé la campagne de Eye of the North, mais cette quête ne peut être obtenue par un joueur que si ce dernier a fini Eye of the North.</li>
	<li>La porte qui mène à Duncan le Sombre ne sera ouverte que si chacun des membres du groupe a battu les 4 boss précédents.</li>
    <li>Une fois Duncan battu, la porte qui mène à son niveau sera fermée même si la quête n'est pas validée. Il faudra reprendre la quête et abattre à nouveau les 4 boss pour y avoir accès.</li>
    <li>Le justicier Thommis et Rand Tissorages sont situés dans le même niveau.</li>
    <li>Le groupe peut a tout moment quitter le bagne sans perdre le bénéfice des boss précédement tués.</li>
    <li>À sa mort, chaque boss (il faut aussi tuer le groupe d'ennemis qui l'accompagne) fait apparaitre 1 coffre de victoire donnant 1 objet à chacun des membres du groupe. Le Coffre du Hiérophante qui apparait après avoir battu Duncan donne 3 objets à chaque membre du groupe dont 1 Restes d'armure de Deldrimor.</li>
    <li>Seule l'ouverture du coffre dans le niveau de Duncan enclenche un chronomètre qui renvoie le groupe dans les Cascades verdoyantes près de l'entrée du bagne. Pour les autres niveaux, mieux vaut revenir en marchant vers le début et prendre la sortie qui mène au niveau supérieur, qui sert de hub, de manière à enchainer rapidement les sous-donjons. Le collectionneur donnant les armes de Destructeur peut servir de marchand.</li>
</ul>

<h2>Cartes du donjon et descriptifs des différentes zones.</h2>

<div align="center"><img src="img/bagne/bagnemap1os1.jpg" alt="map 1" /></div><br /><br />
Cette zone se présente comme un grand hall qui vous permettra de vous engager dans l'un des 4 donjons.<br /><br /><hr /><br />

<div align="center"><img src="img/bagne/bagnejusticierthommissy2.jpg" alt="map 2" /></div><br /><br />
Pour ce premier donjon, le principal problème que vous allez rencontrer, consiste en la capacité des nains à utiliser des compétences de résurrection. Un rodeur avec la compétence "terre gelée" sera donc le bienvenu dans votre groupe ... Comptez environ 40 minutes pour accèder à chacun des deux boss (1h20 en tout donc).<br />
<br /><hr /><br />

<div align="center"><img src="img/bagne/bagneselvebrasmapdl3.jpg" alt="map 3" /></div><br /><br />
Au tour de Selvebras, zone à dominante necromancienne. Particuliarité de cette zone, des esprits de ritu apparaissent : Douleur (fait du dégât), Mélodie des ombres (dégâts + aveuglement), Esprit de désenchantement (ses attaques désenchantent), Récuperation (accélère la disparition des conditions).<br />
Vous y trouverez des nains mais également des morts vivants.<br />
Alors les petites choses à savoir :
<ul>
- Les nains ont des sceaux de rez, un chouilla penible à la longue.<br />
- Les gardiens du sommet sont des élementalistes Terre avec boulversement terrestre et tempête de sable (sorts de zone).<br />
- Un type de necromants Nain fait du heal, ils sont N/D.<br />
</ul>

<font color="#00CCFF"><b>Catégorie 1 :</b></font><br />

<p>Vous verrez un énorme tas de point rouge (4-5 groupes). Les groupes de nains et de morts vivants sont melangés, il faut y aller mollo.
Les groupes de nains sont un peu long à tuer à cause de leur rez et du heal mais ne représentent pas de difficulté majeure.
Si jamais ça devient tendu, n'hésitez pas à reculer.</p>


<font color="#339933"><b>Categorie 2 :</b></font><br />

<p>Vous verrez des clercs enchainés : ce sont des esprits. Quand vous serez à leur portée, des broyeurs de crypte (guerriers), des spectres de crypte (envouteurs) et des fantômes de choc (élementalistes Foudre avec invocation de foudre) vont subitement apparaitre devant vous, ça surprend mais ça se gère assez facilement.</p>

<font color="#CCFF00"><b>Categorie 3 :</b></font><br /><br />

Ici c'est plus dur.
<p>
Vous verrez 4 fantômes de choc. Quand vous êtes à portée du cercle, des spectres de crypte apparaissent mais ça passe. Un peu après, 12 morts vivant niveau 28 apparaissent et là c'est plus difficile. Les morts vivant en question sont des N/A qui se téleportent et ne lancent que des sorts de vol de vie en boucle au corps à corps.</p>

<font color="#FF0000"><b>Final :</b></font>

<p>La partie la plus dure avec le boss Selvebras. Cette salle est composée de plusieurs groupes dont au centre Selvedras au milieu de Nains. Selvedras est un boss necromant niveau 28 qui est assez méchant avec ses dégâts. Il est également accompagné d'un rêveur du Sommet qui utilise Chant de résurection ...<br />
Attention, dans ce groupe de Nain + Selvebras, il y a un necromant bebêtes pour les désacralisations d'enchantements, enchantements profanés.</p>

<p>Le conseil : nettoyer la salle et finir par ce groupe, vous avez assez d'espace pour pouvoir vous faufilez. Vous verrez également des esprits ritualistes alliés, je ne sais pas si ils nous aident (je n'ai pas fait attention) ou si c'est un bug.<br />
Une fois la salle nettoyé, il vous faut taper le groupe Nain vite et fort, Selvebras une fois isolé est très facile.</p>

Une fois vaincu, vous aurez la joie d'ouvrir le coffre du donjon. Comptez environ 1h15 pour terminer cette zone.<br /><br /><hr /><br />

<div align="center"><img src="img/bagne/bagneforgetredungeonmapij5.jpg" alt="map 4" /></div><br /><br />
Forgêtre, zone specialisée dans les dégâts de feu.
<p>Zone remplie de Jinn de feu, beaucoup d'élementalistes feu.<br />

Les élémentaires ondoyants sont à tuer en priorité. Ils utilisent des sorts de zone comme Invocation de Rodgort, chaleur de savane (ne pas rester dedans sinon c'est la mort assurée), tempête de feu.</p>


<p>Il y a 2 zones particulierement difficiles : celles qui encadrées par un carré rouge. Vous y trouverez beaucoup de Nains et il faudra particulièrement faire attention, chaque groupe a des rez et des Nains specialisés dans le soin donc si vous en attirez plusieurs à la fois, ca va comment dire....sentir le roussi.</p>

<p>Forgêtre est un djinn de feu donc insensible à la brulure, il incante ses sorts à une vitesse folle et possède la pluie de météore. Cependant, il ne reflète pas la difficulté du niveau.</p>


Pour passer ce donjon, comptez 1h00 - 1h30.<br /><br /><hr /><br />

<div align="center"><img src="img/bagne/bagneduncantheblackdunglt9.jpg" alt="map finale" /></div><br /><br />
Enfin Duncan le Sombre, boss final du Donjon !<br /><br />

Alors ce niveau est composé de Nains et de morts vivants (esprit N/A specialisé vol de vie via des sorts de contact).<br /><br />

Pas grand chose à dire en plus par rapport aux autres donjons.
Allons à l'essentiel, Duncan : <br /><br />
<div align="center"><img src="img/bagne/dun2qb10ps6.png" alt="placement de l'équipe - duncan" /></div><br /><br />
Donc ici, les rôles sont :
<ul>
- Pour le Guerrier, il doit mourir le plus près de Duncan possible.<br />
- Le necromant se mettra entre le mur et l'escalier, il spammera sceau de chagrin et Vainqueur depouillé sur Duncan : c'est lui qui va le tuer.<br />
- Les moines doivent garder en vie le necromant.<br />
- Les Elementalistes attaquent les esprits de ritualiste, en priorité l'esprit de récuperation mais n'attaquent à aucun prix Duncan.<br />
- Enfin le rodeur s'assois et regarde le spectacle.
</ul>
