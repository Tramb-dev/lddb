<h1>La fosse aux os</h1><br />
<div align="center"><img src="img/uw/zone_fosse.jpg" border="0" class="linked-image" /></div><br />
<br />
La p'tite collection d'ossements privée de Grenth! Non, surtout ne touchez à rien, vous risqueriez de casser le matériel et d'énerver le Dieu de la mort !<br />
Allez, puisque c'est vous, Grenth vous autorise à taper sur les bouts d'os qui font de la résistance... La zone est essentiellement composée de nécros dégen qui ne vous poseront pas trop de problèmes si vous avez de quoi gérer les maléfices.<br />
Faites quand même attention à la structure de l'endroit, rempli de petits passages alambiqués où vous aurez vite fait d'aggro un ou deux groupes en hauteur sans même vous en apercevoir.<br />
<br />
<span id="207"></span><br />
<h3>Esprits emprisonnés</h3><br />
<b>Où la trouver:</b> Fosse aux os, après avoir tué les 3 driders vers la statue de Grenth<br />
<b>Donnée par:</b> Moissonneur de la fosse aux os<br />
<b>Prérequis:</b> Posséder la quête "Restaurez les Monuments de Grenth"<br />
<b>Récompense: </b>10.000 points d'expérience<br />
<b>Difficulté: </b><img src="img/facile.png" border="0" class="linked-image" /><br />
<b>Durée:</b> <img src="img/ico_duree.png" border="0" class="linked-image" /><br />
<br />
<i>Lorsque j'étais dans les pattes des driders terrifiants, de nombreuses âmes ont terminé dans la fosse. La plupart sont perdues pour toujours mais il en reste certaines que la folie n'a pas encore emportées. Je veux les faire sortir de ce royaume du tourment. Ne laissez pas ces esprits libérés se faire anéantir!</i><br />
<br />
Pour plus de sécurité, placez 7 membres de votre groupe en contrebas du Monument, près de la fosse aux tentacules. Le 8ème personnage prend la quête et les rejoindra le plus vite possible.<br />
Les esprits à protéger apparaîtront vers la fosse. Les ennemis arrivent par petites vagues côté Sud et ne devraient pas poser de souci. Vous pouvez aisément les intercepter et vos petits âmes seront tranquilles.<br />
Pour que la quête se termine les âmes doivent atteindre le Monument de Grenth. Continuez tranquillement à empêcher les ennemis de venir par le sud, il n'en vient de nulle part ailleurs. Laissez un moine les escorter si vous préférez être sûrs que rien ne leur arrivera.<br />
Et hop, plus qu'à empocher la récompense !<br />
<br />
<i>Les esprits libérés ont été envoyés dans le Hall du Jugement où Grenth choisira pour eux le destin qui convient le mieux à leur vies de mortels. Le Dieu de la mort vous remercie.</i><br />
