<h1>Le labyrinthe</h1><br />
<div align="center"><img src="img/uw/zone_laby.jpg" border="0" class="linked-image" /></div><br />
<br />
<p>Bienvenue dans l'Outre-Monde !<br />
Grenth vous a téléporté dans son Royaume et vous débarquez au beau milieu de signes cabalistiques. Quelques âmes errent par ici, bloquées par les démons qui ont envahi la zone. Le gros de la difficulté de la zone, autant le dire tout de suite, c'est les Aatxes à lame avec leurs coups à 300. Combinés avec les cauchemars mourants qui suppriment les enchantements, vous avez tout intérêt à faire attention!</p>
Depuis le Labyrinthe, vous avez accès à la Vallée oubliée, aux Débris de glace, et à la Montagne des serpents jumeaux.<br />
<br />
<span id="201"></span><br />
<h3>Le nettoyage de la salle</h3><br />
<b>Où la trouver:</b> près de la zone de départ<br />
<b>Donnée par:</b> Ame perdue<br />
<b>Récompense:</b> 5000 points d'expérience<br />
<b>Difficulté: </b><img src="img/facile.png" border="0" class="linked-image" /><br />
<b>Durée:</b> <img src="img/ico_duree.png" border="0" class="linked-image" /><br />
<br />
<b>Ame perdue-</b> <i>Etrange. Je croyais que losqu'on mourait, on était envoyé dans la Salle de Jugement pour que Grenth décide de notre destin éternel. Au début, j'étais soulagé d'être ici mais je ne veux pas y rester éternellement.<br />
<br />
J'aimerais visiter les alentours mais l'autre extrémité de la pièce est infestée de terribles nocturnes crochus, servicteurs des Tyrachnides. L'un d'entre nous a essayé de passer, mais il s'est fait tuer et son âme s'est fait dévorer!<br />
Aidez-nous. Débarrassez cette pièce des chefs Tyrachnides.</i><br />
<br />
Petite quête d'introduction à l'Outre-Monde pour nous mettre directement dans le bain: il se passe des choses bizarres au royaume de Grenth !<br />
Nettoyez bien les alentours avant de prendre la quête. Cela vous rendra les choses plus faciles. Les Aatxes font très mal mais ne sont guère nombreux, un petit esprit protecteur suffira à contrer les dégâts massifs.<br />
Faites attention aux cauchemars mourants qui enlèvent les enchantements, tirez à vue dès que vous en apercevez un, ils ont très peu de points de vie.<br />
Une fois la salle déblayée, prenez la quête et tuez les trois groupes de trois nocturnes qui arrivent. Suivez ensuite l'un des deux chemins disponibles, peu importe lequel... dans la vaste salle, nous arrivons en vue du premier monument de Grenth. Enchanté. On va peut-être en savoir un peu plus sur ce qui se passe par ici?<br />
Tuez les trois driders vers la statue pour libérer un Moissonneur et empocher votre première récompense. Mais ne vous réjouissez pas trop vite, une longue route vous attend encore.<br />
<br />
<b>Moissonneur du Labyrinthe-</b> <i>L'endroit semble en sécurité désormais. J'espère que Grenth nous accordera audience bientôt.<br />
<br />
Que le Seigneur de la Mort ait pitié de vous dans son royaume.</i><br />
