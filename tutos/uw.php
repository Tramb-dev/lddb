Chaque Moissonneur libéré vous donnera accès à une nouvelle quête. Mais vous n'êtes pas obligé de les faire pour terminer celle qui nous intéresse pour l'instant. Bien que je suppose que vous n'êtes pas ici juste pour faire cette quête, mais bien pour le coffre de fin !<br />
Soyez <b>très prudent</b> en arrivant à chaque nouveau Monument. Nettoyez entièrement le coin avant de vous attaquer aux Driders. Si vous ne le faites pas, vous pourriez bien le regretter si le Moissonneur se fait attaquer et meurt !<br />
<br />
Lorsque vous l'aurez terminée, le Moissonneur, toujours très optimiste, s'exclamera:<br />
<br />
<b>Moissonneur- </b><i>Il y a encore beaucoup à faire.</i><br />
