<br />
<b>Moissonneur du Labyrinthe- </b><i>Les nocturnes crochus ont été bannis et l'ordre revient tranquillement dans l'Outre-Monde. De nouvelles âmes désireuses de témoigner leur éternelle dévotion à Grenth arrivent chaque minute. Menez-les à la Vallée des Oubliés et présentez-les au maire Alegheri. Vous aiderez ainsi à restaurer le royaume de Grenth.</i><br />
