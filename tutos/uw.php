<b>Ennemis spécifiques à la quête:</b><br />
<div align="center"><img src="img/uw/esprits_tortures.jpg" border="0" class="linked-image" /><br />
Esprit torturé</div><br />
<br />
<b>Moissonneur de la Vallée des Oubliés- </b><i>Les démons ont causé tant de destruction lors de mon emprisonnement ! Incapable de suivre les directives de mon maître, j'ai laissé de nombreux esprits contempler un futur qui ne leur était pas destiné. De nouveaux esprits arrivent en ce moment même dans mon village. Ce sont des esprits qui n'auraient jamais dû se trouver ici. Comme beaucoup de morts, ils deviendront haineux et malicieux. Vous devez éliminer ces esprits en colère avant qu'ils détruisent ceux qui sont venus trouver le lieu de repos qui leur est destiné dans la Vallée des oubliés.</i><br />
<br />
OK, cette quête est super simple. Oubliez pour le moment les ennemis apparus en hauteur, et allez vite dézinguer ceux qui tapent sur les pauvres âmes innocentes. Si quelques unes meurent, ce n'est pas grave ! Une fois le danger écarté, retournez vous occuper des âmes en hauteur et allez toucher votre récompense.<br />
<br />
<b>Moissonneur- </b><i>Grenth connaîtra la manière dont vous avez défendu le village. Les bons esprits qui se verront passer le reste de leurs jours dans cet endroit, ne peuvent pas se défendre seuls.</i><br />
