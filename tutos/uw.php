<h3>Les serviteurs de Grenth</h3><br />
<b>Où la trouver:</b> Débris de glace, après avoir tué les 3 driders vers la statue de Grenth<br />
<b>Donnée par:</b> Moissonneur des Débris de glace<br />
<b>Prérequis:</b> Posséder la quête "Restaurez les Monuments de Grenth"<br />
<b>Récompense: </b>10.000 points d'expérience<br />
<b>Difficulté: </b><img src="img/difficile.png" border="0" class="linked-image" /><br />
<b>Durée:</b> <img src="img/ico_duree.png" border="0" class="linked-image" /><br />
<br />
<b>Moissonneur des Débris de glace-</b> <i>Les élémentaires de glace serviteurs de Grenth ont ravagé par la dernière attaque des driders terrifiants. Je vais faire appel au Roi de glace afin qu'il invoque une nouvelle force défensive. Vous devrez le protéger pendant son rituel et vous assurer que les driders n'en profitent pas pour m'attaquer une nouvelle fois.</i><br />
<br />
Cette quête est assez sournoise. Dès que vous l'aurez prise, des monstres vont apparaître juste en contrebas (à l'opposé) du Moissonneur. Le Roi et sa horde de glace se trouvent à peine plus à l'est (en haut de la colline).<br />
Attaquer les ennemis de front est très risqué car ils mettent de gros dégâts, surtout en mode difficile. Une méthode efficace consiste à laisser un Moine se cacher derrière le Moissonneur et le soigner pendant que le groupe de 7 empêchera les monstres de s'attaquer au Roi de glace.<br />
<br />
Attention! Très tôt, vous recevrez un message "Quête réussie". Il n'en est rien ! Déjà parce que vous serez probablement encore en train d'en découdre avec des ennemis, mais il va restera encore une vague après! Celle-ci peut mettre pas mal de temps à venir, mais croyez-moi, elle viendra!<br />
<br />
<i>Beau travail. Cette nouvelle horde d'élémentaires de glace protégera désormais le Hall du Jugement.</i><br />
