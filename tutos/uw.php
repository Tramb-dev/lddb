<b>Où la trouver:</b> Labyrinthe, après avoir tué les 3 driders vers la statue de Grenth<br />
<b>Donnée par:</b> Moissonneur du Labyrinthe<br />
<b>Prérequis:</b> Terminer la quête Le nettoyage de la salle<br />
<b>Récompense: </b>10.000 points d'expérience<br />
<b>Difficulté: </b><img src="img/difficile.png" border="0" class="linked-image" /><br />
<b>Durée:</b> <img src="img/ico_duree.png" border="0" class="linked-image" /><img src="img/ico_duree.png" border="0" class="linked-image" /><img src="img/ico_duree.png" border="0" class="linked-image" /><br />
<b>Ennemis spécifiques à la quête:</b><br />
<div align="center"><img src="img/uw/drider.jpg" border="0" class="linked-image" /><br />
Drider terrifiant</div><br />
<br />
<b>Moissonneur du Labyrinthe- </b><i>Les Tyrachnides ont emprisonné les Moissonneurs de l'Outre-Monde. Sans eux, les Statues de Grenth restent inactives ce qui ne fait que diminuer la domination du dieu de la mort sur ce royaume. Réveillez les six moissonneurs et faites en sorte qu'ils reviennent à leurs statues. Lorsque les statues seront à nouveau actives, il vous sera bien plus facile de vous déplacer dans l'Outre-Monde.</i><br />
<br />
Nous y voilà, le gros morceau de l'aventure Outre-Monde commence dès à présent. Les différents statues de Grenth se trouvent aux endroits suivants :<br />
<br />
<div align="center"><img src="img/uw/monuments.jpg" border="0" class="linked-image" /></div><br />
