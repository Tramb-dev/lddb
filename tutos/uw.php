<h1>Les frayères</h1><br />
<div align="center"><img src="img/uw/zone_frayere.jpg" border="0" class="linked-image" /></div><br />
<br />
<br />
Hum... si vous avez déjà pas mal roulé votre bosse sur Guild Wars, vous avez dû voir un sacré nombre de lieux étranges... Mais là, entrer dans une Frayère !! Berk ! Faites donc attention de ne pas marcher sur des oeufs fécondés, ça colle aux bottes, et j'en sais quelque chose !<br />
<br />
Cette zone n'est pas variée au niveau des ennemis, ce ne sont que des élémentalistes avec de gros dégâts qui meurent tout aussi rapidement qu'ils vous infligeront 300 points de dégâts.<br />
<br />
<span id="210"></span><br />
<h3>La reine des driders terrifiants</h3><br />
<b>Où la trouver:</b> Frayères, après avoir tué les 3 driders vers la statue de Grenth<br />
<b>Donnée par:</b> Moissonneur des Frayères<br />
<b>Prérequis:</b> Posséder la quête "Restaurez les Monuments de Grenth"<br />
<b>Récompense: </b>10.000 points d'expérience<br />
<b>Difficulté: </b><img src="img/facile.png" border="0" class="linked-image" /><br />
<b>Durée:</b> <img src="img/ico_duree.png" border="0" class="linked-image" /><br />
<b>Ennemis spécifiques à la quête:</b><br />
<div align="center"><img src="img/uw/drider.jpg" border="0" class="linked-image" /><br />
Reine des Driders Terrifiants</div><br />
<br />
<b>Moissonneur des Frayères- </b><i>Nous nous approchons de la source de nos ennuis. Plus nous combattons ces démons et plus ils deviennent désespérés. Bientôt la Reine des Tyrachnides arrivera pour détruire les responsables de la mort de sa nichée. C'est à ce moment qu'il faudra la tuer.</i><br />
<br />
Avant de prendre la quête, assurez-vous d'avoir bien nettoyé les Frayères de leurs occupants. Une fois fait, acceptez-la et revenez un peu sur vos pas. La Reine est arrivée, entourée de quatre Gardiens d'obsidienne qui la protègent avec des Protections contre le mal, et se défendent eux-mêmes avec Chair d'obsidienne (oui, oui, 2 élites).<br />
<br />
Commencez par tuer tranquillement les Gardiens jusqu'à-ce que vous puissiez tenir la Reine en dehors des protections. Attention, vous pouvez très bien l'amener dès le début hors de ses retranchement mais un simple sort de zone la fera retourner au bercail. Tuez donc plutôt les 2 gardiens qui se trouvent de votre côté puis terminez la Reine pour terminer cette quête plutôt facile.<br />
<br />
<i>La perte de leur reine a désarçonné les driders. Le règne de Grenth sur l'Outre-Monde finira par être restauré.</i>
