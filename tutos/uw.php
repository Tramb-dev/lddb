<br />
<ul><li><b>Etape 1:</b> placez 7 membres de votre groupe au point de départ indiqué sur la carte (nord est). Le 8ème prend la quête, ce qui vous permet de tuer facilement et rapidement le Gardien cyan sans attirer ses Aatxes sanguinaires.</li><li><b>Etape 2:</b> parlez au moissonneur du Labyrinthe et téléportez-vous à la Vallée oubliée. Revenez au Labyrinthe à pied. Vous tomberez nez-à-nez sur le Gardien mauve sans être gênés par les Aatxes.</li><li><b>Etape 3:</b> téléportez-vous aux Plaines du chaos (ou équivalent: montagne des serpents jumeaux, fosse aux os, frayères) et revenez au Labyrinthe à pied. Vous arriverez près du Gardien bleu que vous pourrez éliminer facilement.</li><li><b>Etape 4:</b> les Aatxes bleus maintenant morts, occupez-vous du Drider vert.</li><li><b>Etape 5:</b> le Gardien rouge est plus délicat à tuer sans avoir les Aatxes sur le dos. Soyez sûr de le puller à l'arc long.</li><li><b>Etape 6:</b> tuez votre dernier gardien (jaune) et c'en sera fini. Ouf!</li></ul><br />
Une fois la quête terminée, le Moissonneur remue bien le couteau dans la plaie: oui, vous n'avez bien tué qu'<b>un </b>Gardien et <b>un </b>Aatxe Sanguinaire !<br />
