<h3>Les quatre cavaliers</h3><br />
<b>Où la trouver:</b> Plaines du chaos, après avoir tué les 3 driders vers la statue de Grenth<br />
<b>Donnée par:</b> Moissonneur des plaines du chaos<br />
<b>Prérequis:</b> Posséder la quête "Restaurez les Monuments de Grenth"<br />
<b>Récompense: </b>10.000 points d'expérience<br />
<b>Difficulté: </b><img src="img/difficile.png" border="0" class="linked-image" /><br />
<b>Durée:</b> <img src="img/ico_duree.png" border="0" class="linked-image" /><br />
<b>Ennemis spécifiques à la quête:</b><br />
<div align="center"><img src="img/uw/chevaucheur.jpg" border="0" class="linked-image" /><br />
Thul Za Dhuum<br />
Madruk Dhuum<br />
Ghozer Dhuum<br />
Kazhad Dhuum</div><br />
<br />
<b>Moissonneur des Plaines du Chaos- </b><i>Avant l'empire de Grenth, lorsqu'un dieu injuste et cruel régnait sur la mort, se trouvait une tour et un trône dans cette vallée. C'est alors que Grenth s'éleva et qu'il tua celui qu'on appelait Dhuum. Il détruisit sa tour en ne laissant que des tempêtes de chaos comme souvenir du pouvoir qui régnait autrefois sur ce territoire.<br />
<br />
J'ai baissé ma vigilance pendant longtemps et je sens y revenir l'atmosphère funeste d'antan. Quatre cavaliers approchent, les cavaliers de Dhuum. Détruisez-les avant qu'ils ne récupèrent l'endroit et qu'ils ne replongent l'Outre-Monde dans le chaos!</i><br />
<br />
Pfffiou... Je ne peux que vous souhaiter bon courage pour cette quête très difficile!<br />
Si vous voulez avoir une chance de la passer, vous devrez au moins avoir un personnage spécialisé pour cette quête.<br />
Les 4 boss sont des envoûteurs et sont chacun accompagnés de 3 driders terrifiants qui infligent des dégâts énormes, notamment en mode difficile.<br />
2 boss arriveront par le sud-ouest, 2 autres par le nord et tueront le Moissonneur en l'espace de quelques secondes si vous les laissez faire. Vous serez donc forcés de séparer votre groupe.<br />
<br />
Une solution communément admise comme l'une des plus sûres est d'avoir 1 à 2 personnes qui retiennent les ennemis d'un côté pendant que le reste du groupe se charge d'éliminer les 2 autres cavaliers.<br />
Pour ce guide, nous avons effectué effectué plusieurs tests et noté que:<br />
<ul><li>En mode difficile, n'espérez pas vous en sortir en split 4/4, à moins d'avoir conçu un build spécialement adapté pour cette quête, ce qui n'est pas des plus pratique pour les autres zones de l'Outre-Monde.</li><li>Il est très difficile de tanker seul 2 cavaliers et leurs troupes. En effet, les boss possèdent le Sceau d'humilité, qui passe à travers une Chair d'obsidienne ou un Briseur de sorts et désactive l'élite pendant une bonne vingtaine de secondes.</li></ul><br />
Voici la solution que nous avons utilisée (notez cependant que beaucoup d'autres moyens de passer cette quête existent. N'hésitez pas à expérimenter par vous-mêmes) :<br />
<ul><li>7 personnes du groupe s'occupent des groupes venant du nord<br />
<img style="display:block;float:right;margin-left:1em;margin-bottom:1em" src="img/uw/boussole.jpg" /></li><li>La 8ème personne, un Moine, se cache derrière le Moissonneur et s'occupe de le protéger contre les 2 groupes venant du sud-ouest. Le build que nous avons utilisé est le suivant:<br />
<ul><li>Briseur de sorts</li><li>Echo des arcanes</li><li>Graine de vie</li><li>Lien de vie</li><li>Esprit de Balthazar</li><li>Aura sacrée</li><li>2 sorts au choix (nous avions pris esprit protecteur et sceau sacré pour des raisons d'utilité dans les autres parties de l'Outre-Monde)</li></ul><br />
</li><li>Le moine seul doit impérativement réussir à boucler Briseur de sorts sur le Moissonneur. N'hésitez pas à avoir un switch tatouage de tête Faveur Divine +3/+1, un bâton enchantements +20%, et au besoin, des Essences de célérité, des oeufs dorés, des Graals de puissance, etc. Faites des tests avant de partir en Outre-Monde pour être sûr de pouvoir boucler le Briseur de sorts.</li><li>Le groupe de 7 vient aider à tuer le groupe du sud ouest une fois qu'il a tué ses 2 cavaliers et ses troupes.</li></ul><br />
Ouf... Vous avez réussi ? Félicitations ! Vous venez de terminer la quête la plus difficile de l'Outre-Monde !<br />
<br />
<b>Moissonneur- </b><i>Maintenant que les cavaliers de Dhuum sont terrassés, je veillerai à ce que l'horreur du passé ne s'installe pas de nouveau dans cet endroit. L'Outre-Monde reposera en paix dans les mains de Grenth.</i><br />
