<h3>Des invités importuns</h3><br />
<b>Où la trouver:</b> Labyrinthe, après avoir tué les 3 driders vers la statue de Grenth<br />
<b>Donnée par:</b> Moissonneur du Labyrinthe<br />
<b>Prérequis:</b> Terminer la quête Le nettoyage de la salle<br />
<b>Récompense: </b>10.000 points d'expérience<br />
<b>Difficulté: </b><img src="img/difficile.png" border="0" class="linked-image" /><br />
<b>Durée:</b> <img src="img/ico_duree.png" border="0" class="linked-image" /><img src="img/ico_duree.png" border="0" class="linked-image" /><br />
<b>Ennemis spécifiques à la quête:</b><br />
<div align="center"><img src="img/uw/drider.jpg" border="0" class="linked-image" /><br />
Gardien des âmes</div><br />
<br />
<b>Moissonneur du Labyrinthe-</b> <i>Les miasmes des Tyrachnides ont imprégné les murs du Labyrinthe. Le Gardien des âmes a lancé un enchantement sur l'Aatxe Revanchard. Aucune lame, aucun marteau et aucun sort au monde ne pourra les atteindre tant que le gardien sera vivant. Tuez-le pour annihiler ses pouvoirs magiques et l'Aatxe Revanchard mourra avec lui. Si vous réussissez, le dieu de la mort tournera son regard bienveillant sur vous.</i><br />
<br />
Bon alors le Moissonneur n'est pas très doué ! Déjà parce que l'Aatxe Revanchard s'appelle en fait Aatxe Sanguinaire, et puis il parle d'<b>un </b>drider et d'<b>un </b>aatxe alors qu'il y en a un sacré paquet de chaque !<br />
Il est conseillé de prendre cette quête le plus tard possible. Plus vous aurez libéré de Monuments avant de la faire, plus elle sera facile pour vous. <br />
<br />
Notez que chaque Gardien est protégé par 3 Driders terrifiants, qui eux peuvent être tués tout de suite.<br />
Dès que le Gardien est mort, les Aatxes succombent en même temps. <b>Mais attention !!</b> Les Aatxes sanguinaires ne meurent que s'ils sont au repos ! Si vous les avez pullés, ils ne voudront bien mourir qu'après vous avoir tués ou si vous parvenez à les faire lâcher l'aggro.<br />
<br />
Référez-vous à la carte suivante pour voir la location des Aatxes invincibles et des Gardiens des âmes :<br />
