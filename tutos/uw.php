<h1>L'Outre-Monde</h1><br />
Suivant l'expérience de votre groupe, et l'efficacité du build utilisé, il faut généralement prévoir entre 2h30 et 3h30 pour avoir accès au coffre de fin de l'Outre-Monde.
<br />
<h2>Généralités sur l'Outre-Monde</h2><br />
<ul>
	<li>Les quêtes sont presque toutes données par les 7 Moissonneurs de Grenth (9 quêtes sur les 10), ce qui vous oblige à prendre la quête "Restaurez les Monuments de Grenth" le plus tôt possible.</li>
	<li>Chaque quête effectuée pour le compte d'un Moissonneur (hormis celui du Labyrinthe) vous donnera accès à un coffre contenant 1 violet pour 1 membre du groupe, ainsi que la possibilité de charmer un familier rare, la Veuve Noire.</li>
    <li>Les moissonneurs ne sont pas que des PNJ lourdingues qu'il faut tout le temps sauver et protéger, ils vous permettent aussi de vous téléporter à chaque Monument que vous avez libéré!</li>
</ul>
