<h2>Cartes du donjon et descriptifs des différentes zones.</h2>

<br />
<div align="center"><img src="img/donjon1/niveau1.jpg" alt="map 1" /></div>
<p>Pour ce premier niveau, l'objectif est simple : vous devez tuer la guivre de siège pour obtenir la clé du donjon. Pour vous faciliter la tache, vous pouvez également tuer le Gardien de la Flamme pour désactiver les pièges de feu.</p>
<p>Si vous le souhaitez, vous pouvez échanger 5 tas de poussière élémentaire contre la carte auprès du collectionneur (près de l'entrée). </p>
<hr /><br />

<div align="center"><img src="img/donjon1/niveau2.jpg" alt="map 2" /></div>
<p>Pour ce second niveau, il faut tuer le Régent des Flammes pour récupérer la clef du Donjon. Pour aller plus vite, et pour plus de facilité, pensez à utilisez le passage du sud. Vous ne passerez pas par la carte du donjon mais ce n'est pas le plus important. <img src="http://www.letolier.net/gw-ally/images/smiles/icon_wink.gif" alt="Clin d&#x2019;oeil"/> </p>
<hr /><br />

<div align="center"><img src="img/donjon1/niveau3.jpg" alt="map 3" /></div>
<p>Pour ce dernier niveau, l'objectif est bien évidement d'atteindre le boss et de le tuer, mais vous allez devoir pour cela traverser plusieurs étapes. 
<ul>
	    - Dans la première pièce vous trouvez en plus des moobs &quot;classiques&quot; des jinns immobiles, les tuer rend les monstres de la salle plus agressifs. Rien de très violent mais attention aux aggros qui peuvent être fatales (surtout en mode hard). <br />
		- Pour récupérer le clé approchez vous de la porte, le boss (un Djinn de Feu) apparait et vous n'avez plus qu'à le tuer. <br />
		- Pensez à tuer les Gardiens de la Flamme pour désactiver les pièges mais attention, ils sont chaque fois bien gardés par des givres et des esprits ardents. <br />
	- Une fois arrivé au boss de fin faites bien attention aux deux boules de feu qui apparaissent quand on l'approche (elles tournent enssuite tout autour de la pièces) et ne prenez pas dans les mains les bombes lachées par le boss. La stupeur peut aider. </p>
</ul>

