<font color="red"><b>Histoire : </b></font>Un groupe de soldats de l'Avant-garde d'Ebon a survécu dans les Catacombes de Kathandrax. Ils ont été pris en embuscade par une horde de Charrs, lors d'une mission qui consistait à récupérer une arme légendaire connue sous le nom de Brise-crâne de Kathandrax. Le capitaine Langmar avait bien fait comprendre à ses troupes que cette arme était plus importante que tout et qu'il fallait la récupérer quoi qu'il en coûte. Brisé et épuisé, le reste de l'Avant-garde n'avait pas fini de peiner, car les grottes étaient infestées d'élémentaires de feu et de créatures horribles. Les survivants étaient peu nombreux et leur unité n'avait plus la force d'atteindre l'artefact. Le pouvoir de celui-ci pouvait donner l'avantage à l'Avant-garde d'Ebon dans sa guerre contre les Charrs. C'est pourquoi nous n'avions pas d'autre choix que de les aider à le retrouver. Protégé par un puissant seigneur élémentaire, le Brise-crâne de Kathandrax reposait caché sous les feux de la Vallée de Sacnoth.<br />
<br />

<font color="red"><b>Objectifs : </b></font>Récupérer le marteau de Kathandrax.<br />
<br />

<font color="red"><b>Points gagnés :</b></font> 1.500 points de l'Avant-Garde en mode normal (200 si déjà fait), 2.250 points de l'Avant-Garde en mode difficile (300 si déjà fait) auxquels s'ajoutent les points Nain gagnés en tuant les moobs. La récompense de la quête est de 7.500 pts d'expérience et 1k500.<br />
