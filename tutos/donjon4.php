<p>Ce donjon comporte un certain nombre d'obstacles :<br />
- Dans la première salle vous pouvez apercevoir la carte de la zone lorsque vous vous tournez vers l'est. Pour l'atteindre, vous devez détruire un mur avec de la matière gélatineuse instable. <br />
- Dans la seconde salle, faites attention aux deux boules qui tournent autour du pilier central. Marchez sur la plaque de pression pour ouvrir la porte.<br />
- La troisième salle contient également des boules, ne les oubliez pas. Pour aller plus vite, prenez le chemin de droite. Vous trouverez la plaque de pression au nord est, après un passage inondé.<br />
- Une fois arrivé aux boss, pensez à ne les tuer que un par un. A leur mort ils se divisent en plusieurs vases, ce qui peut être plutôt violent.
</p>

