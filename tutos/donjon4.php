<font color="red"><b>Histoire : </b></font>Les tremblements de terre ont permis à de nombreuses créatures méconnues par l'homme de sortir des Profondeurs. Des créatures abjectes faites de fluides et de membrane sont l'une de ces calamités. À défaut de mots pour les qualifier, la plupart des aventuriers les appellent des "vases". Ces créatures parasitaires survivent, mutent et se reproduisent dans toutes sortes d'environnements, jusque dans la plus bouillante des terres en fusion. Leur adaptabilité ne leur sert pas seulement à survivre ; non seulement la vase se nourrit de ses proies, mais elle s'attribue les compétences magiques des créatures qu'elle ingère. Les vases les plus vieilles sont ainsi beaucoup plus dangereuses que les jeunes, car elles possèdent potentiellement des compétences d'innombrables créatures et Humains. Les Humains ont trouvé divers emplois à ces corps gélatineux très adaptables et ont commencé à en faire commerce, car leur utilisation s'applique dans de nombreux domaines, dont la médecine, la magie ou l'alchimie.<br />
<br />

<font color="red"><b>Objectifs : </b></font>Récupérer de la matière gélatineuse prismatique dans la fosse à vases.<br />
<br />

